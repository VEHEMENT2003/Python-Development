import numpy as np
import matplotlib.pyplot as plt
a = np.arange(1,20,1.25)
b = np.log(a)
plt.plot(a,b)
plt.plot(a,b,'b',linestyle= 'dashed')
c = np.cos(a)
plt.plot(a.b)
plt.plot(a,c,linestyle = 'dashed')
plt.show()
